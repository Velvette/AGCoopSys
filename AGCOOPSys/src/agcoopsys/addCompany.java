    public void checkInputAddCompany()
    {
        String errorMessages = "";
        ConnectToDatabaseSys connectDB = new ConnectToDatabaseSys();
        int errorCount = 0;
        //FORMAT
        companyAddress1 = validateInputs.formatStringSpaces(companyAddress1);
        companyAddress2 = validateInputs.formatStringSpaces(companyAddress2);
        companyAddress3 = validateInputs.formatStringSpaces(companyAddress3);
        companyContactPerson = validateInputs.formatStringSpaces(companyContactPerson);
        companyName = validateInputs.formatStringSpaces(companyName);
        //THEN CHECK
        
        try
        {
            if(!(connectDB.checkDuplicate(companyName, 1)))
            {
                errorMessages += "Company Name: Already exists\n";
                errorCount++;
            }
        }
        catch(Exception t)
        {
            errorMessages += "Company Name: Already exists\n";
            errorCount++;
        }
        
        if(textCompanyContactPerson.toString().length() != 0)
        if(!(validateInputs.checkForSpecial(companyContactPerson)))
        {
            errorMessages += "Contact Person: Cannot Contain special characters\n";
            errorCount++;
        }      
        
        if(!(validateInputs.checkIfEmpty(companyName)))
        {
            errorMessages += "Company Name: Field cannot be empty\n";
            errorCount++;
        }
                
        if(errorCount == 0)
        {
                String query = "";
                if(choice == 0)
                {
                    query = "insert into company (compname,address1,address2,address3,contactperson,contactno1,contactno2,email)"
                            + " values ('"+ companyName +"','"+ companyAddress1 + "','" + companyAddress2 + "','" + companyAddress3 + "','"
                            + companyContactPerson + "','" + companyContact1 + "','" + companyContact2 + "','" + companyEmail + "')";
                }
                if(choice == 1)
                {
                    query = "update company set compname='"+ companyName +"',address1='"+ companyAddress1+"',address2='"+companyAddress2+"',address3='"+companyAddress3+"'"+
                            ",contactperson='"+companyContactPerson+"',contactno1='"+companyContact1+"',contactno2='"+companyContact2+"',email='"+companyEmail+"' where compid="+companyID;                            
                    choice = 0;
                }
                //System.out.println(query);
                connectDB.accessInputDatabase(query);
        }
        
        else if(errorCount>0)
        {
            System.out.println(errorMessages);
            JOptionPane.showMessageDialog(null, errorMessages, "Error: Company Information", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
