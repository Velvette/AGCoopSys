    public void connect()
    {
        dbDriver = paramDB.getDbClass();
        dbUrl = paramDB.getDbUrl();
        password = paramDB.getPassword(); // CHANGE PASSWORD
        username = paramDB.getName();
                
        try
        {
            Class.forName(dbDriver).newInstance();
            conn = DriverManager.getConnection(dbUrl,username,password);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
	
    public void disconnect()
    {
       try
       {
            conn.close();
       } 
       catch (Exception ex)
       {
            ex.printStackTrace();
       }
    }
    
    public void checkInputEditCompany(String id)
    {
        ResultSet rs;
        this.connect();
        Statement stmt = null;
        
        String query = "select * from company where compid="+id;
        
        try
        {
            stmt = conn.createStatement();
        }
                
	catch (SQLException e)
        {
            e.printStackTrace();
        }
        
        try
        {
            rs = stmt.executeQuery(query);
            if(rs.next())
            {
                textCompanyName.setText(rs.getString("compname"));
                textCompanyAd1.setText(rs.getString("address1"));
                textCompanyAd2.setText(rs.getString("address2"));
                textCompanyAd3.setText(rs.getString("address3"));
                textCompanyContactPerson.setText(rs.getString("contactperson"));
                textCompanyContact1.setText(rs.getString("contactno1"));
                textCompanyContact2.setText(rs.getString("contactno2"));
                textCompanyEmail.setText(rs.getString("email")); 
                companyID = rs.getInt("compid");
            }
            this.disconnect();      
	}
        
        catch (SQLException e)
        {
            e.printStackTrace();
	}   
        choice = 1;
    }
    
